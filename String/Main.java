import java.util.*;
public class Main {
    static String largeOddNum(String s){
        int ind = -1;
        int i;
        for(i=s.length()-1; i>=0; i--){
            if((s.charAt(i)-'0')%2==1){
                ind = i;
                break;
            }
        }
        i=0;
        
    }
    
    
}
